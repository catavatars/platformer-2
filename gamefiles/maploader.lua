function maps.getMapName()
	return maps[currentMap]["name"]
end

function maps.getMap()
	if currentMap <= 0 then
		error("cannot load a map with value less than amount of maps")
	elseif currentMap > #maps then
		error("cannot load a map with value bigger than amount of maps")
	else
		camera:setBounds(0, 0, maps[currentMap]["bounds"], 0)
		--log.send("Loaded map: " .. maps.getMapName(), {255, 128, 0})
		return maps[currentMap]["map"]
	end
end

function maps.lastMap()
	currentMap = currentMap - 1
end

function maps.setMap(num)
	currentMap = num
end

function maps.nextMap()
	currentMap = currentMap + 1
end

return maps
